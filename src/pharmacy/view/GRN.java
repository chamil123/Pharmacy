    public void tablePaymentTotal() {
        DecimalFormat df = new DecimalFormat("##.00");
        DefaultTableModel tb1 = (DefaultTableModel) jTable1.getModel();
        int rowCount = jTable1.getRowCount();
        double totalPayment = 0;
        double totalDiscount = 0;
        double disAmount = 0;

        System.out.println("aaaa : " + rowCount);
        for (int i = 0; i < rowCount; i++) {
            double quantity = Double.parseDouble(tb1.getValueAt(i, 3).toString());
            double price = Double.parseDouble(tb1.getValueAt(i, 4).toString());
            double discount = Double.parseDouble(tb1.getValueAt(i, 5).toString());

            double totAmount = quantity * price;
            disAmount += (totAmount * discount) / 100;

            totalPayment += Double.parseDouble(tb1.getValueAt(i, 6).toString());
            totalDiscount += Double.parseDouble(tb1.getValueAt(i, 5).toString());
        }
        double discount = 0;
        if (!jTextField11.getText().equals("")) {
            discount = Double.parseDouble(jTextField11.getText());
        } else {
            discount = 0;
        }
        double payAmount = 0;
        if (!jTextField12.getText().equals("")) {
            payAmount = Double.parseDouble(jTextField12.getText());
        } else {
            payAmount = 0;
        }
        jLabel7.setText("" + df.format(totalPayment).toString());
        jLabel81.setText("" + totalDiscount);
        jLabel79.setText("" + disAmount);

        double mainDiscount = (totalPayment * discount) / 100;
        jLabel9.setText("" + df.format(totalPayment - mainDiscount).toString());

        jLabel82.setText("" + df.format(payAmount - (totalPayment - mainDiscount)).toString());

    }

