    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        DefaultTableModel dtm = (DefaultTableModel) jTable10.getModel();

        if (jTextField1.getText().trim().length() != 0) {
            try {
                List<Supplier> supplier = SupplierController.searchSupplierByName(jTextField1.getText().trim());

                dtm.setRowCount(0);

                if (supplier != null) {
                    for (int i = 0; i < supplier.size(); i++) {

                        Vector v = new Vector();
                        v.add(supplier.get(i).getSup_name());
                        v.add(supplier.get(i).getSup_companyName());

                        dtm.addRow(v);
                        jPanel2.setVisible(true);
                        jScrollPane6.setVisible(true);
                        jTable10.setVisible(true);

                    }

                    if (dtm.getRowCount() == 0) {
                        jPanel2.setVisible(false);
                        jScrollPane6.setVisible(false);
                        jTable10.setVisible(false);
                    } else {
                        jPanel2.setVisible(true);
                        jScrollPane6.setVisible(true);
                        jTable10.setVisible(true);
                    }
                }
                supplier = null;
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(CreateInvoice.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(CreateInvoice.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            dtm.setRowCount(0);
            jPanel2.setVisible(false);
            jScrollPane6.setVisible(false);
            jTable10.setVisible(false);
        }
    }//GEN-LAST:event_jTextField1KeyReleased

